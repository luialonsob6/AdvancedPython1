"""
Scripts to write sum and substract functions
"""

def sum_two_nums(x,y):
    """
    Sum two nums
    """

    return x + y

def sub_two_nums(x,y):
    """
    Substract two numbers
    """

    return x - y

def divide_two_numbers(x,y):
    """
    Divide two numbers
    """
    return x/y


